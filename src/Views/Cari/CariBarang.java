    private void tblBarangMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblBarangMouseClicked
        // TODO add your handling code here:
        int baris;
        baris = tblBarang.getSelectedRow();
        int jumlah = Integer.parseInt(JOptionPane.showInputDialog("Masukkan Jumlah Barang:"));
        int harga = jumlah * Integer.parseInt(tblBarang.getValueAt(baris, 3).toString());
        transaksi.dtmBarang.addRow(new Object[]{
            tblBarang.getValueAt(baris, 0).toString(),
            tblBarang.getValueAt(baris, 1).toString(),
            jumlah,
            harga
        });
        transaksi.totalBayar +=harga;
        this.dispose();
    }//GEN-LAST:event_tblBarangMouseClicked

