    private void refreshIsiTable() {
        listBarang = barangDAO.selectProduk(
                txtCari.getText(), 
                txtCari.getText()
        );
        dtmBarang = (DefaultTableModel) tblBarang.getModel();
        dtmBarang.setRowCount(0);
        
        listBarang.stream().forEach((data) -> {
            dtmBarang.addRow(new Object[]{
                data.getId_produk(),
                data.getNama(),
                data.getKeterangan(),
                data.getHarga(),
                data.getStok()
            });
        });
        
        if (tblBarang.getRowCount()>0) {
            int baris = tblBarang.getRowCount()-1;
            tblBarang.setRowSelectionInterval(baris, baris);
        }
    }
    
